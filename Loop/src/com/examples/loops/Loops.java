package com.examples.loops;

public class Loops
{
    public static void main(String[] args) {
        //while loop
        int x=0;
        while (x<5)
        {
            System.out.println("Hello World");
            x++;
        }
        System.out.println("");

        //to do while
        int y=0;
        do {
            System.out.println("Hello World!");
            y++;
        }
        while (y<5);
System.out.println("");

//for Loop
        for(int i=0; i<5; i++)
        {
            System.out.println("Hello World");
        }
    }
}
